import turtle

turtle.showturtle()
turtle.pensize(5)
turtle.color("blue")
turtle.circle(100)
turtle.penup()
turtle.goto(0, 20)
turtle.pendown()
turtle.color("green")
turtle.circle(80)
turtle.penup()
turtle.goto(-25, 90)
turtle.pendown()
turtle.color("blue")
turtle.write("Hello World")
turtle.hideturtle()
turtle.done()


